static  void  ConfigLEDGPIO(void);  /* 配置LED的GPIO */

/*********************************************************************************************************
*                                              内部函数实现
**********************************************************************************************************/
/*********************************************************************************************************
* 函数名称：ConfigLEDGPIO
* 函数功能：配置LED的GPIO 
* 输入参数：void 
* 输出参数：void
* 返 回 值：void
* 完成日期：2021年07月01日
* 注    意：
**********************************************************************************************************/
static  void  ConfigLEDGPIO(void)
{
  /* 使能RCU时钟 */
  rcu_periph_clock_enable(RCU_GPIOA);
  rcu_periph_clock_enable(RCU_GPIOC);
  
  gpio_init(LED_G_PORT, GPIO_MODE_OUT_PP, GPIO_OSPEED_50MHZ, LED_G_PIN);
  gpio_bit_reset(LED_G_PORT, LED_G_PIN);

  gpio_init(LED_Y_PORT, GPIO_MODE_OUT_PP, GPIO_OSPEED_50MHZ, LED_Y_PIN);
  gpio_bit_reset(LED_Y_PORT, LED_Y_PIN);
  
  gpio_init(LED_R_PORT, GPIO_MODE_OUT_PP, GPIO_OSPEED_50MHZ, LED_R_PIN);
  gpio_bit_reset(LED_R_PORT, LED_R_PIN);
}

/*********************************************************************************************************
*                                              API函数实现
**********************************************************************************************************/
/*********************************************************************************************************
* 函数名称：InitLED
* 函数功能：初始化LED模块
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 完成日期：2021年07月01日
* 注    意：
**********************************************************************************************************/
void InitLED(void)
{
  ConfigLEDGPIO();  /* 配置LED的GPIO */
}



/* LED便捷控制函数实现 */
void LED1_Toggle(void)
{
    /* LED1（绿色）状态翻转 */
    FlagStatus current = gpio_output_bit_get(LED_G_PORT, LED_G_PIN);
    gpio_bit_write(LED_G_PORT, LED_G_PIN, (FlagStatus)(1U - current));
}

void LED2_Toggle(void)
{
    /* LED2（黄色）状态翻转 */
    FlagStatus current = gpio_output_bit_get(LED_Y_PORT, LED_Y_PIN);
    gpio_bit_write(LED_Y_PORT, LED_Y_PIN, (FlagStatus)(1U - current));
}

void LED3_Toggle(void)
{
    /* LED3（红色）状态翻转 */
    FlagStatus current = gpio_output_bit_get(LED_R_PORT, LED_R_PIN);
    gpio_bit_write(LED_R_PORT, LED_R_PIN, (FlagStatus)(1U - current));
}
