#define _LED_H_

/*********************************************************************************************************
*                                              包含头文件
**********************************************************************************************************/
#include "DataType.h"

/*********************************************************************************************************
*                                              LED引脚定义
**********************************************************************************************************/
#define LED_G_PIN    GPIO_PIN_8    /* 绿色LED，连接到PA8 */
#define LED_G_PORT   GPIOA
#define LED_Y_PIN    GPIO_PIN_6    /* 黄色LED，连接到PC6 */
#define LED_Y_PORT   GPIOC
#define LED_R_PIN    GPIO_PIN_7    /* 红色LED，连接到PC7 */
#define LED_R_PORT   GPIOC

/*********************************************************************************************************
*                                              全局变量声明
**********************************************************************************************************/

/*********************************************************************************************************
*                                              API函数声明
**********************************************************************************************************/
void  InitLED(void);                   /* 初始化LED GPIO配置 */
