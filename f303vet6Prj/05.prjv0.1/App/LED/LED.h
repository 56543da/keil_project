void led_toggle(u32 port, u32 pin, u8 *state);  /* LED状态翻转函数 */

/* LED便捷控制函数 */
void LED1_Toggle(void);                /* LED1（绿色）状态翻转 */
void LED2_Toggle(void);                /* LED2（黄色）状态翻转 */
void LED3_Toggle(void);                /* LED3（红色）状态翻转 */

