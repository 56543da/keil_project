/*********************************************************************************************************
* 函数名称：main
* 函数功能：主函数 
* 输入参数：void
* 输出参数：void
* 返 回 值：int
* 完成日期：2021年07月01日
* 注    意：
**********************************************************************************************************/
int main(void)
{
  InitHardware();   /* 初始化硬件外设模块 */
  InitSoftware();   /* 初始化软件模块 */

  printf("Init System has been finished.\r\n" );  /* 打印系统状态 */
  
  // 设置波形工具的参数显示名称：参数名配置命令
  printf("{{1,SPO2}}\r\n");
  printf("{{2,HR}}\r\n");
  printf("{{3,PI_IR}}\r\n");
  printf("{{4,R_Val}}\r\n");
  printf("{{5,Gain}}\r\n");
  
  printf("LCD Display Test Start.\r\n");
  
  while(1)
  {
    if(Get2msFlag())
    {
      KeyOne_2msTask();
      Clr2msFlag();
    }

    // 处理串口1接收数据 (波形转发与解析)
    UART1_ProcessSPO2Data();

    // 异步更新 UI (PWM 和 Gain)
    // 放在 main loop 中处理，避免在 UART 解析过程中调用耗时的 LCD 刷新函数导致接收丢包
    if(g_u8Uart1UpdateFlag)
    {
        if(g_u8Uart1UpdateFlag & 0x01) // PWM Changed
        {
            UI_UpdatePwm(g_u8PwmRed, g_u8PwmIr);
            g_u8Uart1UpdateFlag &= ~0x01;
        }
        
        if(g_u8Uart1UpdateFlag & 0x02) // Gain Changed
        {
            UI_UpdateGain(g_u8GainCode);
            g_u8Uart1UpdateFlag &= ~0x02;
        }
    }

    // 执行血氧计算
    SPO2_Algo_Process();

    // 检查是否有计算结果
    {
        uint8_t spo2, hr, pi_ir, pi_red;
        float r_val;
        int32_t ac_val;
        if(SPO2_Algo_GetResult(&spo2, &hr, &pi_ir, &pi_red, &r_val, &ac_val))
        {
            SPO2Data_t data;
            data.spo2 = spo2;
            data.heart_rate = hr;
            data.pi = pi_ir;
            data.status = pi_red;
            // 将 R 值 (0.4~1.2) 放大1000倍显示 (400~1200)
            data.filter_status = (uint16_t)(r_val * 1000); 
            data.gain_level = 0xFF;
            data.pwm_red = 0;
            data.pwm_ir = 0;
            
            // 更新 UI
            UI_UpdateData(&data);

           //  打印到串口调试 (适配波形工具参数显示)
           //  格式: [[index,value]]
					
           //  printf("[[1,%d]]\r\n", (int)spo2);
            // printf("[[2,%d]]\r\n", (int)hr);
           //  printf("[[3,%d]]\r\n", (int)pi_ir);
            // printf("[[4,%d]]\r\n", (int)data.filter_status); // R值 * 1000
           //  printf("[[5,%d]]\r\n", (int)ac_val); // AC 峰峰值
        }
    }

    if(Get1SecFlag())              /* 检查1s标志状态 */
    {
      Proc1SecTask();             /* 1s周期处理任务 */
      
      Clr1SecFlag();             /* 清除1s标志 */
    }
    
    UI_Process(); // 处理UI逻辑

  }
}
