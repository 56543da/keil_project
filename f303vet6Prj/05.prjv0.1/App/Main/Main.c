/*********************************************************************************************************
* 函数名称：InitHardware
* 函数功能：初始化硬件模块，在系统启动时调用此函数
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 完成日期：2021年07月01日
* 注    意：
**********************************************************************************************************/
static  void  InitHardware(void)
{  
  SystemInit();        /* 系统初始化 */
  InitRCU();           /* 初始化RCU模块 */
  InitNVIC();          /* 初始化NVIC模块 */  
  InitUART0(115200);   /* 初始化UART模块 - 降速至 115200 */
  InitTimer();         /* 初始化Timer模块 */
  InitLED();           /* 初始化LED模块 */
  InitSysTick();       /* 初始化SysTick模块 */
  InitUART1(115200);   /* 初始化UART1模块 (SPO2接收) - 降速至 115200 */
  InitKeyOne();        /* 初始化KeyOne模块 */
  InitProcKeyOne();    /* 初始化ProcKeyOne模块 */
  LCD_Init();          /* 初始化LCD模块 */
  // SPO2_DisplayInit();  /* 初始化血氧显示 */
  UI_Init();           /* 初始化UI管理器 */
  SPO2_Algo_Init();    /* 初始化血氧算法 */
}



/*********************************************************************************************************
* 函数名称：Proc1SecTask
* 函数功能：1s周期处理任务 
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 完成日期：2021年07月01日
* 注    意：
**********************************************************************************************************/
static  void  Proc1SecTask(void)
{ 
    /* 正常工作指示灯：红色LED每秒闪烁一次 */
    LED3_Toggle();
}
