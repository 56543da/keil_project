/*********************************************************************************************************
* 函数名称：main
* 函数功能：主函数 
* 输入参数：void
* 输出参数：void
* 返 回 值：int
* 完成日期：2021年07月01日
* 注    意：
**********************************************************************************************************/
int main(void)
{
  InitHardware();   /* 初始化硬件外设模块 */
  InitSoftware();   /* 初始化软件模块 */

  printf("Init System has been finished.\r\n" );  /* 打印系统状态 */
  
  printf("LCD Display Test Start.\r\n");
  
  while(1)
  {
    if(Get2msFlag())
    {
      KeyOne_2msTask();
      Clr2msFlag();
    }

    if(Get1SecFlag())              /* 检查1s标志状态 */
    {
      Proc1SecTask();             /* 1s周期处理任务 */
      Clr1SecFlag();             /* 清除1s标志 */
    }

    UART1_ProcessSPO2Data();  /* 处理UART1接收到的SPO2数据 */

  }
}
