static u16 HR_GetValueColor(u8 value)
{
  if(value >= HR_NORMAL_MIN && value <= HR_NORMAL_MAX)
  {
    return SPO2_NORMAL_COLOR;    // ?????????????
  }
  else if((value >= HR_WARNING_LOW && value < HR_NORMAL_MIN) || 
          (value > HR_NORMAL_MAX && value <= HR_WARNING_HIGH))
  {
    return SPO2_WARNING_COLOR;   // ????Z???????
  }
  else if(value < HR_DANGER_LOW || value > HR_DANGER_HIGH)
  {
    return SPO2_DANGER_COLOR;    // ????????????
  }
  else
  {
    return SPO2_VALUE_COLOR;     // ???????
  }
}

