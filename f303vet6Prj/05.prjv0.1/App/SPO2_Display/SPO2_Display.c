void SPO2_DisplayInit(void)
{
  if(!s_displayInitialized)
  {
    // ??????????
    SPO2_ClearDisplayArea();
    
    // ???????
    POINT_COLOR = SPO2_TITLE_COLOR;
    LCD_ShowString(SPO2_DISPLAY_START_X, SPO2_DISPLAY_START_Y, 200, 16, 16, (u8*)"=== SPO2 Monitor ===");
    
    // ??????
    LCD_ShowString(SPO2_DISPLAY_START_X, SPO2_DISPLAY_START_Y + SPO2_LINE_HEIGHT * 1, 120, 16, 16, (u8*)"SpO2:");
    LCD_ShowString(SPO2_DISPLAY_START_X, SPO2_DISPLAY_START_Y + SPO2_LINE_HEIGHT * 2, 120, 16, 16, (u8*)"Heart Rate:");
    LCD_ShowString(SPO2_DISPLAY_START_X, SPO2_DISPLAY_START_Y + SPO2_LINE_HEIGHT * 3, 120, 16, 16, (u8*)"PI:");
    LCD_ShowString(SPO2_DISPLAY_START_X, SPO2_DISPLAY_START_Y + SPO2_LINE_HEIGHT * 4, 120, 16, 16, (u8*)"Status:");
    
    s_displayInitialized = 1;
  }
}

