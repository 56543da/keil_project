void SPO2_DisplayStatus(u8 valid)
{
  u8 displayBuf[32];
  u16 originalColor;
  
  if(!s_displayInitialized)
  {
    SPO2_DisplayInit();
  }
  
  originalColor = POINT_COLOR;
  
  // ????????????
  LCD_Fill(SPO2_DISPLAY_START_X + 80, SPO2_DISPLAY_START_Y + SPO2_LINE_HEIGHT * 4,
           SPO2_DISPLAY_START_X + 140, SPO2_DISPLAY_START_Y + SPO2_LINE_HEIGHT * 5,
           BLACK);
  
  if(valid)
  {
    POINT_COLOR = SPO2_NORMAL_COLOR;
    sprintf((char*)displayBuf, "Valid");
  }
  else
  {
    POINT_COLOR = SPO2_INVALID_COLOR;
    sprintf((char*)displayBuf, "Invalid");
  }
  
  LCD_ShowString(SPO2_DISPLAY_START_X + 80, SPO2_DISPLAY_START_Y + SPO2_LINE_HEIGHT * 4, 
                 60, 16, 16, displayBuf);
  
  POINT_COLOR = originalColor;
}

