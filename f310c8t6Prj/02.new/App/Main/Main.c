static  void  Proc1SecTask(void)
{ 
  static uint8_t led_state = 0;
  static u8 demo_cnt = 0;
  SPO2Data_t spo2Data;
  
  if(Get1SecFlag()) /* ���1s��־״̬ */
  {
    /* ��ת LED (PA6) */
    if(led_state)
    {
      gpio_bit_reset(GPIOA, GPIO_PIN_6);
      led_state = 0;
    }
    else
    {
      gpio_bit_set(GPIOA, GPIO_PIN_6);
      led_state = 1;
    }

    demo_cnt++;
    spo2Data.spo2 = (u8)(90 + (demo_cnt % 11));
    spo2Data.heart_rate = (u8)(60 + ((demo_cnt * 3) % 61));
    spo2Data.pi = (u8)(1 + (demo_cnt % 20));
    spo2Data.status = 0;
    UART0_SendSPO2Data(&spo2Data);

    
    Clr1SecFlag();  /* ���1s��־ */  
  }    
}

