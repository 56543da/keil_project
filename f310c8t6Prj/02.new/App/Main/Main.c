/*********************************************************************************************************
* 函数名称：InitHardware
* 函数功能：初始化硬件模块，如系统时钟等调用此函数
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
**********************************************************************************************************/
static  void  InitHardware(void)
{  
  InitNVIC();          /* 初始化NVIC模块 */  
  InitUART0(115200);   /* 初始化UART模块 */
  InitLED();           /* 初始化LED (PA6) */
  SPO2_Driver_Init();  /* 初始化SPO2驱动 (PA1, PC13, PC14) */
  InitTimer();         /* 初始化Timer模块 - 开启定时器中断 */
  InitWatchdog();      /* 初始化看门狗，防止因意外开启导致的复位 */
}

/*********************************************************************************************************
* 函数名称：InitLED
* 函数功能：初始化LED (PA6) 
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2026年01月08日
* 注    意：
**********************************************************************************************************/
static void InitLED(void)
{
  rcu_periph_clock_enable(RCU_GPIOA);
  gpio_mode_set(GPIOA, GPIO_MODE_OUTPUT, GPIO_PUPD_NONE, GPIO_PIN_6);
  gpio_output_options_set(GPIOA, GPIO_OTYPE_PP, GPIO_OSPEED_50MHZ, GPIO_PIN_6);
  
  /* 初始状态输出低电平 */
  gpio_bit_reset(GPIOA, GPIO_PIN_6);
}

/*********************************************************************************************************
* 函数名称：InitWatchdog
* 函数功能：初始化独立看门狗
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2026年02月03日
* 注    意：LSI约为40kHz，配置超时时间约19.2s
**********************************************************************************************************/
static void InitWatchdog(void)
{
  /* 开启对 FWDGT_PSC 和 FWDGT_RLD 寄存器的写访问 */
  fwdgt_write_enable();
    
  /* 配置预分频器为 256, 40kHz / 256 = 156.25Hz */
  /* 配置重装载值为 3000, 超时时间 = 3000 / 156.25 ≈ 19.2s */
  fwdgt_config(3000, FWDGT_PSC_DIV256);
    
  /* 开启看门狗 */
  fwdgt_enable();
}

/*********************************************************************************************************
* 函数名称：main
* 函数功能：主函数 
* 输入参数：void
* 输出参数：void
* 返 回 值：int
* 创建日期：2021年07月01日
* 注    意：
**********************************************************************************************************/
int main(void)
{
  static uint32_t run_time_sec = 0;
  SPO2Data_t spo2Data;
  uint8_t spo2, hr, pi;
  uint8_t debug_msg[] = "DEBUG: Raw UART Send Test\r\n";

  InitHardware();   /* 初始化硬件等相关模块 */
  InitSoftware();   /* 初始化软件模块 */
	
  /* 上电后强制发送一包测试数据，验证串口和链路是否通 */
  {
    SPO2Data_t testData;
    testData.spo2 = 99; // 修改为99以区分测试包
    testData.heart_rate = 80;
    testData.pi = 10;
    testData.status = 0;
    UART0_SendSPO2Data(&testData);
  }
  
  // 发送原始字符串测试
  WriteUART0(debug_msg, sizeof(debug_msg)-1);

  printf("System Start (72MHz)...\r\n");
  printf("CK_SYS: %d, CK_AHB: %d, CK_APB1: %d\r\n", 
          rcu_clock_freq_get(CK_SYS), 
          rcu_clock_freq_get(CK_AHB), 
          rcu_clock_freq_get(CK_APB1));
	
  while(1)
  {
    fwdgt_counter_reload(); /* 喂狗 */

    // 检查是否有新的计算结果 (由Timer中断驱动)
    if(SPO2_GetResult(&spo2, &hr, &pi))
    {
        spo2Data.spo2 = spo2;
        spo2Data.heart_rate = hr;
        spo2Data.pi = pi;
        spo2Data.status = 0;
        UART0_SendSPO2Data(&spo2Data);
    }
    
    // 每秒打印一次运行时间 (Timer4中断产生1s标志)
    if(Get1SecFlag())
    {
        Clr1SecFlag();
        run_time_sec++;
        printf("Run Time: %d sec\r\n", run_time_sec);
        
        // 翻转LED指示运行
        if(run_time_sec % 2 == 0)
            gpio_bit_set(GPIOA, GPIO_PIN_6);
        else
            gpio_bit_reset(GPIOA, GPIO_PIN_6);
    }
  }
}
